# List of numbers
Num = [1, 23, 43, 45, 63, 62, 56, 35, 2]

# List of letters
letter = ["a", "b", "d", "f"]

# Function to print length of a list
def print_len(lst):
    # Print the length of the list
    print(len(lst))
    
    # Return the list
    return lst

# Function call with Num list
print_len(Num)
print_len(letter)
